package Day10Swichloop;

public class Loops01 {
    public static void main(String[] args) {
        for (int i=1    ;i<5    ;i++   ){
                     System.out.println("HI");}

        for (int i=11;i<14;i++){
            System.out.println(i);
        }

        for (int i=41;i>22;i--){if (i%2==0){
            System.out.print(i+" ");}}


        for (int i=18;i<57;i++){if (i%2!=0){
            System.out.print(i+" ");}}




    }}
