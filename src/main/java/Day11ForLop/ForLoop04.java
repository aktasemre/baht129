package Day11ForLop;

public class ForLoop04 {
    public static void main(String[] args) {
        int k=0;
        for (int i=0;i<10;i++) {

            k = k + i;
        }
        System.out.println(k);
        int car=1;
        for (int i=1;i<10;i++){

          car=  car*i;}
        System.out.println(car);

    }
}
